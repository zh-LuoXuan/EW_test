#include <stdio.h>
#include "demo_gpio.h"
#include "demo_uart.h"
#include "demo_delay.h"


/***********************************************************************************************************************
Global variables and functions
***********************************************************************************************************************/

int main()
{
    uint32_t msCnt;     // count value of 1ms
    uint8_t flag;

    //-----------------------------------------------------------------------
    // Systick setting
    //-----------------------------------------------------------------------
    SystemCoreClockUpdate();
    msCnt = SystemCoreClock / 1000;
    SysTick_Config(msCnt);
    delay_init(SystemCoreClock); //延时初始化
    Led_Init();


	  UART0_Config();
    flag = Reset_Status_Get();
    printf("RESF = 0x%02X\n", flag);	//打印芯片复位类型值

    if (flag == 0x00)
    {
        NVIC_SystemReset();
    }

    while (1)
    {
        delayMS(250);
        LED_Toggle;
    }
}




